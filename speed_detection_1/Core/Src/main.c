// --- ホールセンサ状態を読み取り、ステップ制御 ---
void Update_Hall_And_Commutate()
{
  uint8_t hall = (HALL_A_READ() << 2) | (HALL_B_READ() << 1) | (HALL_C_READ() << 0);
  uint16_t duty = Get_Duty_From_ADC();
  uint16_t duty_lowside = 1000;
  Set_Step(hall, duty, duty_lowside);
}

