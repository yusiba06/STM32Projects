/* USER CODE BEGIN 0 */

uint16_t Get_Duty_From_ADC()
{
  HAL_ADC_Start(&hadc1);
  HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
  uint32_t adc_val = HAL_ADC_GetValue(&hadc1);
  HAL_ADC_Stop(&hadc1);

  // 12bit ADC (0〜4095) を PWM の Period 値にスケーリング
  return (uint16_t)((adc_val * htim1.Init.Period) / 4095);
}

